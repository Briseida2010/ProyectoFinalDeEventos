    public void cargarTabla(String busqueda){
        listaPersonas = clsPrograma.listarPonentesNoInscritos(busqueda,idEvento);
        DefaultTableModel dtm = (DefaultTableModel)tblPersona.getModel();
        FrmMaster.limpiarTabla(tblPersona);
        for(clsEntidadPersona objPersona : listaPersonas){
            Object[] fila = new Object[6];
            fila[0] = objPersona.getIdPersona();
            fila[1] = objPersona.getNombres();
            fila[2] = objPersona.getApellidos();
            fila[3] = objPersona.getEmail();
            
            dtm.addRow(fila);
        }
        tblPersona.setModel(dtm);
    }

