    public void seleccionarTabla(){
        int fila = tblPersona.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(null, "Debes seleccionar una fila");
        }else{
            _objPersona = listaPersonas.get(fila);
        }
    }
    
