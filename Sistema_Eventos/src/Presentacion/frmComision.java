     @Override
    public void cargarTabla(String busqueda){
        listaComision = clsComision.listarComision(busqueda);
        DefaultTableModel dtm = (DefaultTableModel)tblComision.getModel();
        FrmMaster.limpiarTabla(tblComision);
        for(clsEntidadComision objComision : listaComision){
            Object[] fila = new Object[15];
            fila[0] = objComision.getIdPersona();
            fila[1] = objComision.getIdEvento();
            fila[2] = objComision.getTipo();


            dtm.addRow(fila);
        }
        tblComision.setModel(dtm);
    }
    
      @Override
    public void seleccionarTabla(){
        int fila = tblComision.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(null, "Debes seleccionar una fila");
        }else{
            txtPersona.setText(String.valueOf(_objComision.getIdPersona()));
            txtEvento.setText(String.valueOf(_objComision.getIdEvento()));
            txtEvento.setText(String.valueOf(_objComision.getTipo()));
        }
    }
    
    @Override
    public void guardar(){
        clsEntidadComision objComision = new clsEntidadComision();
        objComision.setIdPersona(Integer.parseInt(txtPersona.getText()));
        objComision.setIdEvento(Integer.parseInt(txtEvento.getText()));
        objComision.setTipo(txtTipo.getText());
        clsComision.agregarComision(objComision);
        cargarTabla("");
    }
    
    @Override
    public void modificar() {
        clsEntidadComision objComision = new clsEntidadComision();
        objComision.setIdPersona(_objComision.getIdPersona());
        objComision.setIdEvento(_objComision.getIdEvento());
        objComision.setTipo(txtTipo.getText());
        clsComision.modificarComision(objComision);
        cargarTabla("");
    }
    
    
