    @Override
    public void cargarTabla(String busqueda){
        listaPersonas = clsPersona.listarPersonas(busqueda);
        DefaultTableModel dtm = (DefaultTableModel)tblPersona.getModel();
        FrmMaster.limpiarTabla(tblPersona);
        for(clsEntidadPersona objPersona : listaPersonas){
            Object[] fila = new Object[6];
            fila[0] = objPersona.getIdPersona();
            fila[1] = objPersona.getNombres();
            fila[2] = objPersona.getApellidos();
            fila[3] = objPersona.getEmail();
            
            dtm.addRow(fila);
        }
        tblPersona.setModel(dtm);
    }
    
    @Override
    public void seleccionarTabla(){
        int fila = tblPersona.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(null, "Debes seleccionar una fila");
        }else{
            _objPersona = listaPersonas.get(fila);
            txtNombre.setText(_objPersona.getNombres());
            txtApellidos.setText(_objPersona.getApellidos());
            txtEmail.setText(_objPersona.getEmail());  
        }
    }
    
    @Override
    public void guardar(){
        clsEntidadPersona objPersona = new clsEntidadPersona();
        objPersona.setNombres(txtNombre.getText());
        objPersona.setApellidos(txtApellidos.getText());
        objPersona.setEmail(txtEmail.getText());
        
        clsPersona.agregarPersona(objPersona);
        cargarTabla("");
    }
    
    @Override
    public void modificar() {
        clsEntidadPersona objPersona = new clsEntidadPersona();
        objPersona.setIdPersona(_objPersona.getIdPersona());
        objPersona.setNombres(txtNombre.getText());
        objPersona.setApellidos(txtApellidos.getText());
        objPersona.setEmail(txtEmail.getText());
        
        
        clsPersona.modificarPersona(objPersona);
        cargarTabla("");
    }
  
